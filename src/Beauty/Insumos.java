    private void jButtonListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListoActionPerformed
        boolean sw2=false;
        if(jTextDescripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó la descripción del insumo");
            sw2=true;
        }
            
        else if(jTextFieldMonto.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó el precio del insumo");
            sw2=true;
        }else if(jTextFieldFecha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ingresó la fecha de ingreso");
            sw2=true;
        }
        
       
       else if(sw2==false){
            
            try {
                con = DriverManager.getConnection(url, user, clave);
                stmt = con.createStatement();
                stmt.executeUpdate("INSERT INTO insumos VALUES('0', '"+jTextDescripcion.getText()+"','"+jTextFieldMonto.getText()+"','"+jTextFieldCantidad.getText()+"',"
                        + " '"+jTextFieldFecha.getText()+"')");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "error");
                Logger.getLogger(Insumos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            
            
            
            
            
            
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
            modelo.setRowCount(0);
            
            jTextFieldMonto.setText("");
            jTextDescripcion.setText("");
            
            jLabelSumas.setText("");
            
            refrescarPagina("");
            sumarDatosNeto();
        }
    }//GEN-LAST:event_jButtonListoActionPerformed

