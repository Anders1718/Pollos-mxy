    void sumarDatosNeto(){
        int cont=0;
        int filas = TablaDatos.getRowCount();
        for(int i=0; i<filas; i++){
            cont += Integer.parseInt((String) TablaDatos.getValueAt(i, 2));
        }
        jLabelTotal.setText(String.valueOf(cont));
      }
    
    
